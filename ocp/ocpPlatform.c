int32_t ocpPlatformID(char *buffer, uint32_t maxsize){

    uint32_t k;
    char *p;

    p = xplatformID;
    k = 0;
    while( (*p != 0) && (k < maxsize) ){
        *buffer++ = *p++;
        k++;
    }

    return k;
}
