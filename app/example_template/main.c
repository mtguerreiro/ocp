//=============================================================================
/*-------------------------------- Includes ---------------------------------*/
//=============================================================================
#include <stdint.h>
#include <stdio.h>
#include <stdlib.h>
#include <pthread.h>

/* OCP */
#include "ocp/ocpIf.h"

/* App */
#include "app.h"

/* socket for OCP interface */
#include "ocpIfThread.h"
//=============================================================================

//=============================================================================
/*---------------------------------- Main -----------------------------------*/
//=============================================================================
//-----------------------------------------------------------------------------
int main(void){

    pthread_t ocpIfThreadHandle;

    ocpIfInitialize();

    appInit();

    pthread_create( &ocpIfThreadHandle, NULL, ocpIfThread, NULL);

    pthread_join( ocpIfThreadHandle, NULL);

    exit(0);
}
