void circBuf_write( circBuf_t* buffer, int32_t data ) {

	buffer->mem[buffer->head] = data;
	buffer->head = (buffer->head + 1) % buffer->size;
}
//---------------------------------------------------------------------------
void circBuf_SetTail( circBuf_t* buffer, int32_t distance ) {

	if (buffer->head >= distance) buffer->tail = buffer->head - distance;
	else buffer->tail = buffer->size - (distance - buffer->head);
}
